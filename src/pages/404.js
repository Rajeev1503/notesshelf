export async function getStaticProps() {
  const { semesters } = await graphcms.request(GET_ALL_SEMESTER);

  if(!semesters) {

    return {
        props: {
            semesters: null
        }
    }

  }

  return {
    props: {
      semesters: semesters,
    },
    revalidate: 10,
  };
}
